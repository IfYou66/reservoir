import request from '@/utils/request'

// 查询培训列表
export function listJoinTraining(query) {
  return request({
    url: '/reservoir/joinTraining/list',
    method: 'get',
    params: query
  })
}

// 打分
export function edit(data) {
  return request({
    url: '/reservoir/joinTraining',
    method: 'put',
    data: data
  })
}
