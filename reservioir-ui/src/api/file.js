import request from "../utils/request";

// 查询档案 列表
export function getFileById(query) {
  return request({
    url: '/file/getFileById',
    method: 'get',
    params: query
  })
}

// 删除文件
export function delFile(query) {
  return request({
    url: '/file/delFile',
    method: 'get',
    params: query
  })
}
