package com.ruoyi.system.api.domain;

import java.util.Date;

import com.ruoyi.common.core.web.domain.BaseEntity;

/**
 * 文件信息
 * 
 * @author ruoyi
 */
public class SysFile extends BaseEntity
{
    private String fId;
    /**
     * 文件地址
     */
    private String fFilePath;
    private String fOldFileName;
    private String fNewFileName;
    private Date fCreateTime;
    private String fCreateBy;
    
    /**
     * 水库id
     */
    private Long fReservoirInfoId;

    
    public Long getfReservoirInfoId() {
		return fReservoirInfoId;
	}

	public void setfReservoirInfoId(Long fReservoirInfoId) {
		this.fReservoirInfoId = fReservoirInfoId;
	}

	public String getfId() {
        return fId;
    }

    public void setfId(String fId) {
        this.fId = fId;
    }

    public String getfFilePath() {
        return fFilePath;
    }

    public void setfFilePath(String fFilePath) {
        this.fFilePath = fFilePath;
    }

    public String getfOldFileName() {
        return fOldFileName;
    }

    public void setfOldFileName(String fOldFileName) {
        this.fOldFileName = fOldFileName;
    }

    public String getfNewFileName() {
        return fNewFileName;
    }

    public void setfNewFileName(String fNewFileName) {
        this.fNewFileName = fNewFileName;
    }

    public Date getfCreateTime() {
        return fCreateTime;
    }

    public void setfCreateTime(Date fCreateTime) {
        this.fCreateTime = fCreateTime;
    }

    public String getfCreateBy() {
        return fCreateBy;
    }

    public void setfCreateBy(String fCreateBy) {
        this.fCreateBy = fCreateBy;
    }

    @Override
    public String toString() {
        return "SysFile{" +
                "fId='" + fId + '\'' +
                ", fFilePath='" + fFilePath + '\'' +
                ", fOldFileName='" + fOldFileName + '\'' +
                ", fNewFileName='" + fNewFileName + '\'' +
                ", fCreateTime=" + fCreateTime +
                ", fCreateBy='" + fCreateBy + '\'' +
                '}';
    }
}
